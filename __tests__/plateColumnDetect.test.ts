import { describe, it, expect } from "vitest";
import { detectPlateColumn } from "@/lib/plateParser";

describe("detectPlateColumn — اكتشاف عمود اللوحة في ملف الداتا", () => {
  it("يختار عمود اللوحة مش عمود التاريخ حتى لو نسبة اللوحات متوسطة واسم العمود مفهوش كلمة لوحة", () => {
    // نفس شكل مشكلة المستخدم: أول عمود تواريخ (تاريخ التسجيل)، وعمود اللوحة
    // اسمه مفهوش «لوحة» وفيه شوية صفوف مش لوحات — الاكتشاف القديم كان بيرجع
    // لأول عمود (التاريخ).
    const headers = ["تاريخ التسجيل", "المركبة", "الحي"];
    // عمود اللوحة نسبته ~37% لوحات (3 من 8) — أقل من عتبة 0.5 القديمة، فكان
    // الاكتشاف يفشل ويرجع لأول عمود (التاريخ). عمود التاريخ نسبته 0%.
    const rows = [
      { "تاريخ التسجيل": "20251019", "المركبة": "ردا2812", "الحي": "النهضة" },
      { "تاريخ التسجيل": "20251019", "المركبة": "باه7157", "الحي": "العزيزية" },
      { "تاريخ التسجيل": "20251020", "المركبة": "دكم4488", "الحي": "الروضة" },
      { "تاريخ التسجيل": "20251020", "المركبة": "غير معروف", "الحي": "السلامة" },
      { "تاريخ التسجيل": "20251021", "المركبة": "لا يوجد", "الحي": "الفيصلية" },
      { "تاريخ التسجيل": "20251021", "المركبة": "بدون لوحة", "الحي": "المروة" },
      { "تاريخ التسجيل": "20251022", "المركبة": "تحت المعاينة", "الحي": "النسيم" },
      { "تاريخ التسجيل": "20251022", "المركبة": "قيد الفحص", "الحي": "الصفا" },
    ];
    expect(detectPlateColumn(headers, rows)).toBe("المركبة");
  });

  it("عمود تواريخ صرف (8 أرقام) مايتحسبش عمود لوحة أبداً", () => {
    const headers = ["تاريخ التسجيل"];
    const rows = [
      { "تاريخ التسجيل": "20251019" },
      { "تاريخ التسجيل": "20251020" },
    ];
    // مفيش عمود لوحة حقيقي — يرجع للاحتياطي (أول عمود) بس مش لازم يعتبره لوحة
    // المهم إنه لو فيه عمود لوحة تاني يفضّله؛ هنا مفيش، فالنتيجة أول عمود.
    expect(detectPlateColumn(headers, rows)).toBe("تاريخ التسجيل");
  });

  it("لسه بيحترم عمود فيه لوحات نضيفة (نسبة عالية)", () => {
    const headers = ["الحي", "رقم اللوحة"];
    const rows = [
      { "الحي": "النهضة", "رقم اللوحة": "ردا2812" },
      { "الحي": "العزيزية", "رقم اللوحة": "باه7157" },
    ];
    expect(detectPlateColumn(headers, rows)).toBe("رقم اللوحة");
  });
});
