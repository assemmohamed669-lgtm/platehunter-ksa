import { describe, it, expect } from "vitest";
import { normalizePlate } from "@/lib/plateParser";

/**
 * قفل هوية normalizePlate (تطبيع المطابقة) — الخط الأحمر.
 * الدالة دي مستخدمة في الفرز/المطلوب/التشييك للمطابقة، **مش** لتطبيع الكلام.
 * أي استخراج/refactor للمحرك الصوتي **ممنوع** يغيّر مخرجاتها. القِيَم دي
 * اتلقطت من الكود الحالي (٢٠٢٦-٠٧-١٧) قبل بدء الاستخراج، ولازم تفضل ثابتة بعده.
 */
const CASES: Array<[string, string]> = [
  ["أ ب ح 1234", "ابح1234"],   // مسافات + همزة ألف
  ["ابح1234", "ابح1234"],
  ["أ-ب-ح-1234", "ابح1234"],   // شرطات
  ["5052حبك", "حبك5052"],       // مقلوبة (أرقام قبل حروف)
  ["حبك5052", "حبك5052"],
  ["أإآ", "ااا"],                // توحيد الألف
  ["ى", "ي"],                    // ألف مقصورة → ياء
  ["حـبـك1234", "حبك1234"],     // تطويل
  ["١٢٣٤", "1234"],              // أرقام عربية-هندية
  ["ABC123", "0123"],            // لاتيني يتشال، الأرقام تفضل
  ["حبك 50 52", "حبك5052"],
  ["", ""],
  ["ا ب ج 12", "ابج0012"],       // مافيش تحقّق حروف — بيسيب ج
  ["  حكل0800  ", "حكل0800"],
  ["بح٨", "بح0008"],
  ["NKD 5678", "5678"],
];

describe("normalizePlate — قفل الهوية (الخط الأحمر: تطبيع المطابقة)", () => {
  for (const [input, expected] of CASES) {
    it(`«${input}» → «${expected}»`, () => {
      expect(normalizePlate(input)).toBe(expected);
    });
  }
});
