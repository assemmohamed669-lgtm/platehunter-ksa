-- ─────────────────────────────────────────────────────────────────────────
-- استعادة مندوب اتمسح بروفايله «نص نص»:
-- لو الحذف مسح صف البروفايل (public.profiles) لكن حساب الدخول في auth.users
-- فضل موجود (بيظهر «الإيميل مستخدم بالفعل» عند إعادة الإضافة)، الملف ده بيرجّع
-- البروفايل مربوط بنفس حساب الدخول الموجود — فالمندوب يرجع يظهر في لوحة الأدمن
-- وكل تسجيلاته (recordings) تفضل مربوطة بيه وسليمة (المعرّف id ما اتغيّرش).
--
-- غيّر السطرين المعلّمين بـ ← بس (الاسم + الإيميل المسجّل حالياً)، وشغّل مرة واحدة
-- في Supabase → SQL Editor.
-- ─────────────────────────────────────────────────────────────────────────

insert into public.profiles (id, username, email, role, is_active, subscription_end)
select u.id,
       'اسم المندوب',                              -- ← اكتب اسم المندوب هنا (اللي هيظهر في الأدمن)
       u.email,
       'agent',
       true,
       (now() + interval '1 month')::date          -- اشتراك شهر من النهاردة (عدّله زي ما تحب)
from auth.users u
where lower(u.email) = lower('agent-email@example.com')  -- ← اكتب إيميله المسجّل حالياً هنا
on conflict (id) do nothing;
